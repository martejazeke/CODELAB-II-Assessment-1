import math

# Task 1: Ceil of 2.3
a1 = 2.3
ceil_result = math.ceil(a1)
print(f"For a = {a1}, ceil: {ceil_result}")

# Task 2: Floor of 2.3
a2 = 2.3
floor_result = math.floor(a2)
print(f"For a = {a2}, floor: {floor_result}")

# Task 3: Factorial of 5
a3 = 5
factorial_result = math.factorial(a3)
print(f"For a = {a3}, factorial: {factorial_result}")

# Task 4: Power of 2 to the power of 3
a4 = 2
power_result = math.pow(a4, 3)
print(f"For a = {a4}, 2^3: {power_result}")

# Task 5: Square root of 16
a5 = 16
sqrt_result = math.sqrt(a5)
print(f"For a = {a5}, square root: {sqrt_result}")
